				parte de Unify Ltda., tienen los siguientes derechos, los cuales pueden ejercer
				en cualquier momento:<span></span></span></p>

				<p ><span>1.<span > </span></span><span>Conocer los Datos Personales sobre los cuales Unify Ltda. está
				realizando el Tratamiento. De igual manera, el Titular puede solicitar en
				cualquier momento, que sus datos sean actualizados o rectificados, por ejemplo,
				si encuentra que sus datos son parciales, inexactos, incompletos, fraccionados,
				induzcan a error, o aquellos cuyo Tratamiento esté expresamente prohibido o no
				haya sido autorizado.<span></span></span></p>

				<p ><span>2.<span > </span></span><span>Solicitar prueba de la autorización otorgada a Unify Ltda. para
				el Tratamiento de sus Datos Personales.<br>
				<br>
				<span></span></span></p>

				<p ><span>3.<span > </span></span><span>Ser informado por Unify Ltda., previa solicitud, respecto del
				uso que ésta le ha dado a sus Datos Personales.<span></span></span></p>

				<p ><span>4.<span > </span></span><span>Presentar ante la Superintendencia de Industria y Comercio
