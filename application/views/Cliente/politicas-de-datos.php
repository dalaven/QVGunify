				<p  ><b><span  >Disposiciones Especiales para el Tratamiento
				de Datos Personales</span></b><span  ><span></span></span></p>

				<p ><span>1.<span > </span></span><b><span>Tratamiento de Datos Personales de Naturaleza
				Sensible</span></b><span><span></span></span></p>

				<p  ><span>El Tratamiento de los Datos Personales de naturaleza sensible
				está prohibido por la ley, salvo que se cuente con autorización expresa, previa
				e informada del Titular, entre otras excepciones consagradas en el Artículo 6º
				de la Ley 1581 de 2012.<span></span></span></p>

				<p  ><span>En este caso, además de cumplir con los requisitos establecidos
				para la autorización, Unify Ltda. informará al Titular:<span></span></span></p>

				<p  ><span>• que por tratarse de datos sensibles no está obligado a
				autorizar su Tratamiento.<br>
				<br>
				• cuáles de los datos que serán objeto de Tratamiento son sensibles y la
				finalidad del Tratamiento.<span></span></span></p>

				<p  ><span>Adicionalmente, Unify Ltda. tratará los datos sensibles
				recolectados bajo estándares de seguridad y confidencialidad correspondientes a
				su naturaleza. <span></span></span></p>

				<p ><span>2.<span > </span></span><b><span>Tratamiento de Datos Personales de Niños,
				Niñas y Adolescentes</span></b><span><span></span></span></p>

				<p  ><span>Según lo dispuesto por el Artículo 7º de la Ley 1581 de 2012 y
				el artículo 12 del Decreto 1377 de 2013, Unify Ltda. sólo realizará el
				Tratamiento, correspondientes a niños, niñas y adolescentes, siempre y cuando
				este Tratamiento responda y respete el interés superior de los niños, niñas y
				adolescentes y asegure el respeto de sus derechos fundamentales.<span></span></span></p>

				<p  ><span>Cumplidos los anteriores requisitos, Unify Ltda. deberá obtener
				la Autorización del representante legal del niño, niña o adolescente, previo
				ejercicio del menor de su derecho a ser escuchado, opinión que será valorada
				teniendo en cuenta la madurez, autonomía y capacidad para entender el
				asunto.<span></span></span></p>

				<p  ><b><span  >Procedimiento para Atención y Respuesta a
