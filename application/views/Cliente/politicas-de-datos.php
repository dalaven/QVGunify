					Política de Protección de Datos Personales
				</h1>


				<p  ><b><span  >Alcance</span></b><span  ><span></span></span></p>

				<p  ><span>Esta Política de Protección de Datos
				Personales se aplicará a todas las Bases de Datos y/o Archivos que contengan
				Datos Personales que sean objeto de tratamiento por parte de la marca UNIFY. <span></span></span></p>

				<p  ><b><span  >Identificación del Responsable del Tratamiento
				de Datos Personales</span></b><span  ><span></span></span></p>

				<p  ><span>Unify Ltda con domicilio en la Autopista Norte
				No. 108-27 oficina 1505 de la ciudad de Bogotá D.C., Colombia. Teléfono +57 (1)
				4193400.<span></span></span></p>

				<p  ><b><span  >Definiciones</span></b><span  ><span></span></span></p>

				<p  ><span>• </span><b><span  >Autorización:</span></b><span> Consentimiento previo, expreso e informado del Titular para
				llevar a cabo el Tratamiento de Datos Personales.<br>
				<br>
				• <b>Aviso de Privacidad:</b> Comunicación
				verbal o escrita generada por el Responsable, dirigida al Titular para el
				Tratamiento de sus Datos Personales, mediante la cual se le informa acerca de
				la existencia de las Políticas de Tratamiento de información que le serán
				aplicables, la forma de acceder a las mismas y las finalidades del Tratamiento
				que se pretende dar a los datos personales.<br>
				<br>
				• <b>Base de Datos: </b>Conjunto organizado
				de Datos Personales que sea objeto de Tratamiento.<br>
				<br>
				• <b>Clientes:</b> Persona natural o
				jurídica, pública o privada con los cuales la marca tiene una relación
				comercial. <br>
				<br>
				• <b>Consumidores:</b> Persona natural que
				consume los bienes producidos por la marca.<br>
				<br>
				• <b>Dato Personal:</b> Cualquier
				información vinculada o que pueda asociarse a una o varias personas naturales
				determinadas o determinables. Son algunos ejemplos de datos personales los
				siguientes: nombre, cédula de ciudadanía, dirección, correo electrónico, número
				telefónico, estado civil, datos de salud, huella dactilar, salario, bienes,
				estados financieros, etc.<br>
				<br>
				• <b>Dato sensible:</b> Información que
				afecta la intimidad del Titular o cuyo uso indebido puede generar su
				discriminación, tales como aquellos que revelen el origen racial o étnico, la
				orientación política, las convicciones religiosas o filosóficas, la pertenencia
				a sindicatos, organizaciones sociales, de derechos humanos o que promueva
				intereses de cualquier partido político o que garanticen los derechos y garantías
				de partidos políticos de oposición así como los datos relativos a la salud, a
				la vida sexual y los datos biométricos, entre otros, la captura de imagen fija
				o en movimiento, huellas digitales, fotografías, iris, reconocimiento de voz,
				facial o de palma de mano, etc.<br>
				<br>
				• <b>Encargado del Tratamiento:</b> Persona
				natural o jurídica, pública o privada, que por sí misma o en asocio con otros,
				realice el Tratamiento de Datos Personales por cuenta del Responsable del
				Tratamiento. En los eventos en que el Responsable no ejerza como Encargado de
				la Base de Datos, se identificará expresamente quién será el Encargado.<br>
				<br>
				• <b>Responsable del Tratamiento:</b>
				Persona natural o jurídica, pública o privada, que por sí misma o en asocio con
				otros, decida sobre la Base de Datos y/o el Tratamiento de los datos.<br>
				<br>
				• <b>Reclamo:</b> Solicitud del Titular del
				dato o de las personas autorizadas por éste o por la Ley para corregir,
				actualizar o suprimir sus datos personales o para revocar la autorización en
				los casos establecidos en la Ley.<br>
				<br>
				• <b>Términos y Condiciones:</b> marco
				general en el cual se establecen las condiciones para los participantes de
				actividades promocionales o afines.<br>
				<br>
				• <b>Titular:</b> Persona natural cuyos
				Datos Personales sean objeto de Tratamiento.<br>
				<br>
				• <b>Transferencia:</b> La transferencia de
				datos tiene lugar cuando el Responsable y/o Encargado del Tratamiento de datos
				personales, ubicado en Colombia, envía la información o los datos personales a
				un receptor, que a su vez es Responsable del Tratamiento y se encuentra dentro
				o fuera del país.<br>
				<br>
				• <b>Transmisión:</b> Tratamiento de Datos
				Personales que implica la comunicación de los mismos dentro o fuera del
				territorio de la República de Colombia cuando tenga por objeto la realización
				de un Tratamiento por el Encargado por cuenta del Responsable.<br>
				<br>
				• <b>Tratamiento:</b> Cualquier operación o
				conjunto de operaciones sobre Datos Personales, tales como la recolección,
				almacenamiento, uso, circulación o supresión.<span></span></span></p>

				<p  ><b><span  >Principios Aplicables al Tratamiento de Datos
				Personales</span></b><span><br>
				<br>
				Para el Tratamiento de los Datos Personales, Unify Ltda. aplicará los
				principios que se mencionan a continuación, los cuales constituyen las reglas a
				seguir en la recolección, manejo, uso, tratamiento, almacenamiento e
				intercambio, de datos personales:<span></span></span></p>

				<p  ><span>• <b>Legalidad:</b>
				El Tratamiento de datos personales se realizará conforme a las disposiciones
				legales aplicables (Ley Estatutaria 1581 de 2012 y sus decretos
				reglamentarios).<br>
				<br>
				• <b>Finalidad:</b> Los datos personales
				recolectados serán utilizados para un propósito específico y explícito el cual
				debe ser informado al Titular o permitido por la Ley. El Titular será informado
				de manera clara, suficiente y previa acerca de la finalidad de la información
				suministrada.<span></span></span></p>

				<p  ><span>• <b>Veracidad
				o Calidad:</b> La información sujeta al Tratamiento de Datos Personales debe
				ser veraz, completa, exacta, actualizada, comprobable y comprensible.<br>
				<br>
				• <b>Libertad:</b> La recolección de los
				Datos Personales solo podrá ejercerse con la autorización, previa, expresa e
				informada del Titular.<br>
				<br>
				• <b>Transparencia:</b> En el Tratamiento
				de Datos Personales se garantiza el derecho del Titular a obtener en
				cualquier momento y sin restricciones, información acerca de la existencia de
				datos que le conciernan.<br>
				<br>
				• <b>Acceso y circulación restringida:</b>
				El Tratamiento de datos personales solo podrá realizarse por las personas
				autorizadas por el Titular y/o por las personas previstas en la Ley.<br>
				<br>
				• <b>Seguridad:</b> Los Datos Personales
				sujetos a Tratamiento se manejarán adoptando todas las medidas de seguridad que
				sean necesarias para evitar su pérdida, adulteración, consulta, uso o acceso no
				autorizado o fraudulento.<br>
				<br>
				• <b>Confidencialidad:</b> Todos los
				colaboradores que trabajen con Unify Ltda. están obligados a guardar reserva
				sobre la información personal a la que tengan acceso con ocasión de su trabajo
				con la marca.<span></span></span></p>

